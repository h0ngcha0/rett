%%%_* Module declaration =======================================================
-module(rett_util).

%%%_* Exports ==================================================================
-export([ get_env/2
        ]).

%%%_* Code =====================================================================
get_env(Env, Default) ->
  case application:get_env(rett, Env) of
    {ok, Val} -> Val;
    undefined -> Default
  end.

